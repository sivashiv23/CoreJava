
public class Modulo {

	public static void main(String[] args) {
		System.out.println("bigger by smaller- it prints the remainder");
		int a = 15;
		int b = 3;
		System.out.println(" modulo " + a % b);

		System.out.println("smaller by bigger- it prints the number");
		int a1 = 3;
		int b1 = 15;
		System.out.println(" modulo " + a1 % b1);
	}

}
